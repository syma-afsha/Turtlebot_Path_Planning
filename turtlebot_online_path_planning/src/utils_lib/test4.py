class RRTStar:
    def __init__(self, data, state_validity_checker, start, goal, probability, step_q, gamma,bounds = [-10,10,-10]):
        self.map = np.array(data).reshape(98,99)
        self.state_validity_checker = state_validity_checker
        self.start = start
        self.goal = goal
        self.step = step_q
        self.V = np.array([start])
        self.E = {}
        self.t = 5
        self.Path = []
        self.probability = probability
        self.gamma = gamma
        self.bounds = bounds
        # Other initializations remain the same


    def build(self, q0, n):
        for _ in range(n):
            qrand = self.rand_conf(self.map.shape[0], self.probability, self.goal)
            qnear, qnew = self.nearest_and_new(qrand)
            
            if self.is_collision_free(qnear, qnew) and self.map[int(qnew[1]), int(qnew[0])] != 0.5:
                self.V = np.append(self.V, [qnew], axis=0)
                self.E[tuple(qnew)] = tuple(qnear)
                self.map[int(qnew[1]), int(qnew[0])] = 0.5

                # rewire
                self.rewire(qnew)

                if np.sqrt((qnew[0] - self.goal[0]) ** 2 + (qnew[1] - self.goal[1]) ** 2) < self.t:                    
                    print("Solution found")
                return self.V, self.E, _

            print("No solution found, iterate again!")
        return [], [], 0

    # same as RRT's extend()
    def nearest_and_new(self, qrand):
        # Ensure qrand is a 2D array before using it in cdist
        qrand_reshaped = qrand.reshape(1, -1)  # Reshape qrand to be 2D if not already
        distances = cdist(self.V, qrand_reshaped)
        qnear_index = np.argmin(distances)
        qnear = self.V[qnear_index]

        # Calculate qnew direction and ensure it's a 2D point
        direction = (qrand - qnear) / np.linalg.norm(qrand - qnear)
        qnew = qnear + self.step * direction

        # Ensure qnew is within bounds before clipping
        qnew = np.clip(qnew, [self.bounds[0], self.bounds[2]], [self.bounds[1], self.bounds[3]])

        # Before returning, make sure qnew has the correct shape
        if qnew.shape != (2,):
            raise ValueError(f"Unexpected qnew shape: {qnew.shape}. Expected (2,)")

        return qnear, qnew
    # IMPORTANT
    def rewire(self, qnew):

        # find nearby nodes
        # dist(vertex, qnew) < max_distance
        near_indices = np.where(cdist(self.V, [qnew]) < self.gamma)[0]

        # iterate found nodes
        for i in near_indices:

          # path indices bounds
            if i < len(self.Path):

                # NO collision
                if self.is_collision_free(self.V[i], qnew):

                  # new_cost (nearby node -> goal, qnew -> goal)
                    potential_cost = self.total_distance(self.Path[i:]) + self.total_distance([qnew, self.goal])

                    # new_cost < curr: cost
                    if potential_cost < self.total_distance(self.Path[i:]) + self.total_distance([self.Path[i], self.goal]):

                        # update tree
                        # new edge = new node (qnew) to the nearby node
                        # new path
                        self.E[tuple(qnew)] = tuple(self.V[i])
                        self.Path[i] = qnew

    def rand_conf(self, bounds, p, goal):
        x_min, x_max, y_min, y_max = self.bounds
        random_point = np.array([
            np.random.uniform(x_min, x_max),
            np.random.uniform(y_min, y_max)
        ])
        
        return random_point
            

    def is_collision_free(self, qnear, qnew, divisor=7):
        
        magnitude = np.linalg.norm(qnew - qnear)
        direction = (qnew - qnear) / magnitude
        for i in range(1, divisor + 1):
            intermediate_point = qnear + (i / divisor) * direction
            # Check if the intermediate point is valid
            if not self.state_validity_checker.is_valid(intermediate_point):
                return False
        return True
    def fill_path(self):
        self.Path = [tuple(self.goal)]
        current = self.Path[-1]
        i = 0
        while not (current[0] == self.start[0] and current[1] == self.start[1]):
            self.Path.append(self.E[current])
            current = tuple(self.E[current])
            i += 1

        return self.Path[::-1]




    def smooth_path(self):
        smooth = [self.start]

        while not (smooth[-1][0] == self.goal[0] and smooth[-1][1] == self.goal[1]):
            for i in range(0, len(self.Path)):
                if self.is_collision_free(np.array(smooth[-1]), self.Path[i], divisor=200):
                    smooth.append(list(self.Path[i]))
                    break
        return smooth

    def total_distance(self, path):
        total = 0
        for i in range(0, len(path) - 1):
            total = total + np.sqrt((path[i][0] - path[i + 1][0]) ** 2 + (path[i][1] - path[i + 1][1]) ** 2)
        return total

            
        
                
                

        
    


